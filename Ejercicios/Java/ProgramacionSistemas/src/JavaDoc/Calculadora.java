package JavaDoc;

/**
 * Clase Calculadora
 * Permite realizar operaciones básicas
 *
 * @author Alberto
 * @version 1.0
 * @since 2025
 */
public class Calculadora {

    /**
     * Suma dos números
     *
     * @param a primer número
     * @param b segundo número
     * @return resultado de la suma
     */
    public int sumar(int a, int b) {

        return a + b;

    }

}